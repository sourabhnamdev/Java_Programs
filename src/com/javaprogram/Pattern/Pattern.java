package com.javaprogram.Pattern;

public class Pattern {
	 public static void main(String[] args) {
	        for (int i = 1; i <= 6; i++) {
	            for (int j = i; j < i * 2 ; j++) {
	                System.out.print("*");
	                
	            }
	            System.out.println();
	        }
	    }
}
 